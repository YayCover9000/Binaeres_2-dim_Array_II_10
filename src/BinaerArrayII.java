public class BinaerArrayII {
    public static void ausgeben(int matrix[][]) {
        for (int j = 0; j < matrix.length; j++) {
            System.out.print("\nZeile " + j + " :");
            for (int i = 0; i < matrix.length; i++) {
                System.out.print(" " + i);
            }

        }
    }
}
